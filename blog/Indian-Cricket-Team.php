			<h2>Indian Cricket Team</h2>
	
			<p>
				One of the more common coaching cliches in sport is some variant of “It is more difficult to stay on top than it is to get there”. The year 2016 saw the number one ranked Test side changing hands frequently. India began the year at the top but were swiftly replaced by Australia, who then were supplanted by Pakistan. India finished the year routing England at home, and establishing a firm grip at the number one spot, opening a sizable lead over second-ranked Australia.<br><br>

India had a golden run in 2016, winning nine of 12 Tests (three drawn), and losing only 11 limited overs matches while reaching the semifinal of World T20. Their on-field success was driven by Ravichandran Ashwin and Virat Kohli primarily, but plenty of supporting acts played significant roles and the 2016 success should be broadly shared amongst all the team members.<br><br>

AP<br>
What does 2017 hold for Virat Kohli and Co? AP<br>
As the new year dawns, India are number one in Tests, third in ODIs and second in T20Is. None too shabby!<br><br>

If 2016 was the year Kohli established himself as the leader of the side and India as a side to be reckoned with after middling returns in the years prior, 2017 would appear to be the year when Kohli would further mould the side in his image and the team puts itself together for a run at elusive success abroad in 2018, with tours of South Africa, Australia, New Zealand, and England inked in. But what does 2017 hold for them? How far could they advance the gains of 2016? Virat Kohli is wont to say that this current Indian lineup are young enough to form the core for six to eight years and hence could achieve lasting success.<br><br>

On the Test calendar, India are penciled in to face Bangladesh and Australia at home and Sri Lanka away. (One never knows when and where India-Sri Lanka contests would take place. These things change more often than a politician running for office.) Bangladesh have been a vastly improved side, (despite their ICC ranking) and showed it by drawing the series at home against England, while coming within a few runs of winning the series 2-0. They will be up against it when they play their first ever Test on Indian soil, but the quality of their spinners should keep them in the contest. However, the experience and skills of Indian fast bowlers could be the deciding factor as India are expected to brush past them.<br><br>

When the new look Australia embark on the four Tests series, plenty will recall 'Homeworkgate' of 2013 and considering Australia's reverses in Sri Lanka and against South Africa at home, would pencil in a 4-nil shellacking. However, Australia do possess something that other tourists to India in 2016 did not – the raw pace of Mitchell Starc which could take the pitch out of the equation. In addition, the ever-improving Josh Hazlewood and the experience of Nathan Lyon provides Australia a chance to compete in Indian conditions. The question then will be of Aussie batsmen finding sufficient form to challenge India. In all likelihood, Kohli and his men should register another series victory, but it will be a lot closer than it appears on paper.<br><br>

Even as Sri Lanka have found new batsmen to take over from Kumar Sangakkara and Mahela Jayawardene, they have plenty of holes in their side and wouldn't seriously mount any threat to Kohli's streak of series wins. However, these Tests should serve as a way for India to identify their top four-fivce pacers that could provide the much needed teeth for their away tours in 2018. India should also be keen to use Ashwin as their number six batsman as often as possible, so that they have more options when they travel abroad in 2018.<br><br>

With Ajinkya Rahane and Rohit Sharma returning from injuries, and Karun Nair showing his ability in Tests with an unbeaten 303, the middle order is spoiled for choices. India would do well to identify a third opener (if Shikhar Dhawan does not recover his form) to go along with Murali Vijay and KL Rahul. As they use 2017 as proving ground for their away tours of 2018, India would do well to identify a steady slip cordon as well. As captain, Kohli has placed himself in the slips for the pacers, and Vijay has appeared there as well with mixed results. Rahane has been the go-to slipper for spinners and has become a presence at gully for pacers. India will need sure hands they can trust and develop over the next 12 months that can grow together next six to eight years.<br><br>

The big ticket ODI item in 2017 is the Champions Trophy in England. India return as the defending champions and the three ODIs in January against a revamped England side ought to tell them a lot about the composition they need to invest for the Champions Trophy, with an eye to the World Cup in 2019, also to be held in England. The biggest question surrounding India in ODIs is MS Dhoni.<br><br>

It has become obvious that Dhoni is no longer the batsman he used to be a few years ago. Dhoni relinquished his Test captaincy when he realised he no longer could or wanted to contribute to the Test side, but the decision is much harder in ODIs where Dhoni has built a long-lasting legacy. It will be up to the selectors and the team management to decide whether Dhoni could play any meaningful role in 2019. If not, it would be prudent to blood in a youngster as soon as possible so that they have sufficient exposure before the World Cup. One obvious replacement for Dhoni is the youngster from Delhi, Rishabh Pant. It would also be better if Dhoni were to leave the scene in ODIs for Kohli to assume the mantle of captain across all the formats.<br><br>

The year 2016 was all about India bossing the visitors at home, and 2017 appears to be more of the same with an added incentive of planning for the future. If they get all their ducks in a row and stay relatively healthy, they could very well make the job of staying at the top a lot less difficult for themselves.<br><br>


				
