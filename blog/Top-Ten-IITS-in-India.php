			<h2>Top Ten IITs in India</h2>
	
			<p>
			

				The IITs are autonomous engineering institutions that are governed by the rules and regulations laid down by the All India Council of Technical Education (AICTE) established in 1945. Officially declared as the &ldquo;institutions of national importance&rdqou; in 1961, they are a dream for every aspiring Indian engineer and scientist. Every year, a million students appear for the IIT-JEE (Joint Entrance Examination).; out of which about 10,000 get admission in the IITs.<br><br>

Here's a list of IIT colleges in India, which have been ranked on the basis of reputation, faculty, and citations in the media.<br><br><br><br>

The Best IIT Colleges in India<br><br>

1. IIT Bombay<br><br>

Located in the Powai area of Mumbai, it was founded in 1958 and is the second oldest among the IITs. While the flagship B.Tech course of IIT Bombay draws thousands of young aspirants every year, its courses in social sciences such as sociology and philosophy are also popular. It also offers 17 interdisciplinary centers where students can take a list of courses. From the last 5 years, 75% of the top 100 ranks in the Joint Entrance Examination (JEE) choose IIT Bombay. If you like growth and opportunities along with an active social life, IIT Bombay has all of it to offer. It conducts India's largest college festivals, which has participants coming in from all over the world. This has consistently been among the top IIT colleges in India!<br><br>



2. IIT Delhi<br><br>

When it was formally established in 1961, this institution was named as the College of Engineering and Technology. It was given the tag of ‘IIT' in 1963, following which it has evolved into one of the most reputed centers of higher education in the country. Ranked after to Bombay and Kanpur, IIT Delhi attracts young talent from across the country. With a sprawling campus in the heart of the Indian capital city, it ensures that students get the best of everything and go away with an enriching experience.<br><br>



3. IIT Kharagpur
<br><br>
Established in 1951, IIT Kharagpur is one of the most prestigious educational institutions in the country and also the oldest. With a campus spread across 2100 acres, it presently is also the largest among all IITs. Official statistics indicate that this institution has the highest student enrollment rate too.<br><br>



4. IIT Kanpur<br><br>

Next in the hierarchy comes the 57-year-old IIT Kanpur, located near Kalyanpur village in Kanpur, Uttar Pradesh. The foundation of this institution is largely credited to the generous assistance provided by nine US universities, which were a part of the Kanpur Indo-American Programme (KIAP). An IIT aspirant needs to secure a top rank in the JEE Advanced Examination to get into this college. Being a public research institution, IIT Kanpur has been involved in a number of projects of social and public utility. One of the most recent projects undertaken by it is the National Air Quality Index, which was one of the biggest projects undertaken by the The Minister of Environment.<br><br>

Time and again, it has proved itself to be one of the top IIT colleges in India!<br><br>



5. IIT Roorkee<br><br>

Established way back in 1847, this is the oldest technical institution in the continent of Asia. A formal ‘university' status was accorded to it in 1949, and 52 years later, in 2001, it was given the status of an IIT. Besides engineering, one is also likely to be impressed by the humanities and applied sciences departments of this institution.<br><br>



6. IIT Madras (Chennai)<br><br>

Set up in 1959, with assistance from erstwhile West Germany, IIT Chennai was the third of its kind to be set up immediately after India's independence. With a glorious history of producing achievers in all fields of human endeavour, IIT Chennai has become a top destination for today's budding engineers.<br><br>



7. IIT Guwahati<br><br>

Located in the tranquil city of Guwahati, Assam, it was established in the year 1994. This institution has fast evolved into a top-class public institution in the country. The scenic beauty surrounding the campus makes the thought of joining IIT Guwahati all the more exciting!<br><br>

Visit the official website here
<br><br>
8. IIT Hyderabad<br><br>

IIT Hyderabad started functioning from August 2008; however, the foundation stone of was laid by Smt. Sonia Gandhi, Hon'ble Chairperson of UPA in its main campus in Kandi later. With a 500-plus student population, IITH has a large number of activities to offer: sports, cultural, technology, and entrepreneurship. The institute wants to provide technology, solutions and trained manpower for the betterment of the people of India.
<br><br>


9. IIT Gandhinagar<br><br>

Located in Palaj, Gandhinagar, Gujarat on the banks of river Sabarmati, IIT Gandhinagar strives to offer the best undergraduate and graduate education in India. With state-of-the-art  infrastructure, it emphasizes on liberal arts, project-oriented learning, compulsory courses in design and life sciences, diversity and globalization. The institute also offers Ph.D. in Literature, Philosophy, Psychology, Sociology, Political Sciences and Social Epidemiology.<br><br>



10. IIT (BHU) Varanasi<br><br>

IIT (BHU), Varanasi, has 13 departments and three inter-disciplinary schools. The departments of Metallurgical Engineering, Electronics Engineering, Mining Engineering, and Chemical Engineering have been recognized as Centers of Advanced Studies (CAS) by the UGC of India. The college conducts three major national level festivals namely, TECHNEX (Technical Festival), KASHI-YATRA (Cultural Festival) and SPARDHA (Sports Festival), which ensure that students have a vibrant campus life.<br><br> 



